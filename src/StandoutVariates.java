public class StandoutVariates {

    public static void main(String[] args){
        System.out.println("Hello new test");

        int age;
        age = 37;

        System.out.println(age);

        age = 30 + 10;

        System.out.println(age);

        age = (7 * 5) + 2;

        System.out.println(age);

        System.out.println("The age is: " + age + ", congratulations.");
    }

}